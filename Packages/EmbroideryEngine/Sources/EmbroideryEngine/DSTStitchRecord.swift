/// One 3-byte Tajima DST stitch record: a relative needle movement of up to
/// ±121 embroidery units per axis, plus jump / color-change flags.
///
/// Bit layout (https://edutechwiki.unige.ch/en/Embroidery_format_DST):
///
///     BYTE  |  7  |  6  |  5  |  4  ||  3  |  2  |  1  |  0
///     ------|------------------------------------------------
///       1   | y+1 | y-1 | y+9 | y-9 || x-9 | x+9 | x-1 | x+1
///       2   | y+3 | y-3 | y+27| y-27|| x-27| x+27| x-3 | x+3
///       3   | c2  | c1  | y+81| y-81|| x-81| x+81| set | set
public struct DSTStitchRecord: Hashable, Sendable {
    /// Largest encodable delta magnitude per axis, in embroidery units
    /// (Catroid `MAX_DISTANCE`). The bound is inclusive: ±121 is legal
    /// (ADR-012 — Catty's strict-comparison guard is a bug, not ported).
    public static let maxDelta = 121

    /// Delta → ternary bit pattern (0x1 = +1, 0x2 = −1, 0x4 = +3, 0x8 = −3,
    /// … 0x200 = −81). Indices 0…121 hold +0…+121, 122…242 hold −1…−121.
    /// Ported verbatim from Catroid `DSTFileConstants.CONVERSION_TABLE`
    /// (AGPL-3.0, © The Catrobat Team); the balanced-ternary derivation
    /// exists only as the regeneration test, per US-103.
    static let conversionTable: [UInt16] = [
