    ]

    /// The three record bytes, in file order.
    public let bytes: [UInt8]

    /// Encodes a delta in embroidery units. Both `dx` and `dy` must be
    /// within `-maxDelta...maxDelta`; US-105's interpolation keeps stream
    /// deltas inside that range, so violations are programmer errors.
    public init(dx: Int, dy: Int, isJump: Bool = false, isColorChange: Bool = false) {
        precondition(
            abs(dx) <= Self.maxDelta && abs(dy) <= Self.maxDelta,
            "DST record deltas must be within ±\(Self.maxDelta) embroidery units"
        )
        // Bit scatter ported from Catroid DSTStitchPoint.setDSTPointBytes.
        let xValue = Self.tableEntry(for: dx)
        let yValue = Self.tableEntry(for: dy)

        let byte0 = ((yValue & 0x1) << 3 | (yValue & 0x2) << 1 | (yValue & 0x10) >> 3 | (yValue & 0x20) >> 5) << 4
            | (xValue >> 2) & 0xC | xValue & 0x3
        let byte1 = ((yValue & 0x4) << 1 | (yValue & 0x8) >> 1 | (yValue & 0x40) >> 5 | (yValue & 0x80) >> 7) << 4
            | (xValue >> 4) & 0xC | (xValue >> 2) & 0x3
        var byte2 = 0x03 | (yValue >> 5) & 0x10 | (yValue >> 3) & 0x20 | (xValue >> 6) & 0xC
        if isJump {
            byte2 |= 0x80
        }
        if isColorChange {
            byte2 |= 0xC0
        }
        bytes = [UInt8(byte0), UInt8(byte1), UInt8(byte2)]
    }

    /// Encodes the movement between two absolute positions. Positions are
    /// already-rounded embroidery units, so the delta is round-then-subtract
    /// by construction (ADR-012: never convert the difference).
    public init(
        from previous: EmbroideryPoint,
        to current: EmbroideryPoint,
        isJump: Bool = false,
        isColorChange: Bool = false
    ) {
        self.init(
            dx: current.x - previous.x,
            dy: current.y - previous.y,
            isJump: isJump,
            isColorChange: isColorChange
        )
    }

    private static func tableEntry(for delta: Int) -> Int {
        Int(conversionTable[delta < 0 ? -delta + 121 : delta])
    }
}
